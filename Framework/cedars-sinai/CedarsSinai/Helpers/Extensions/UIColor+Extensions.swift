import UIKit

extension UIColor {
    public static var csLightBlue: UIColor {
        return UIColor(red: 219 / 255, green: 233 / 255, blue: 238 / 255, alpha: 1)
    }
    
    public static var csRed: UIColor {
        return UIColor(red: 220 / 255, green: 30 / 255, blue: 52 / 255, alpha: 1)
    }
    
    public static var csGrayText: UIColor {
        return UIColor(red: 146 / 255, green: 146 / 255, blue: 146 / 255, alpha: 1)
    }

    public static var csBlue: UIColor {
        return UIColor(red: 0, green: 104/255, blue: 129/255, alpha: 1)
    }
}
